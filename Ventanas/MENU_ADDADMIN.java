    private void GUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GUARDARActionPerformed
        // TODO add your handling code here:
        String [] datos = new String [2];
        CSVCode miBD= new CSVCode(0," ");
        
        if (this.accion=="nuevo"){
            try {
                if (miBD.newAdmin(TextUser.getText(),TextPass.getText())){
                    JOptionPane.showMessageDialog(this, "Guardado con exito");
                    datos[0] = TextUser.getText();
                    datos[1] = TextPass.getText();
                    tDatos.addRow(datos);
                }else
                    JOptionPane.showMessageDialog(this, "Error al guardar");
            } catch (IOException ex) {
                Logger.getLogger(MENU_ADDADMIN.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(MENU_ADDADMIN.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if(this.accion=="editar"){
            //System.out.println("estoy en editar");
            int fila = TablaAdmin.getSelectedRow();
            tDatos.setValueAt(TextUser.getText(), fila, 0);
            tDatos.setValueAt(TextPass.getText(), fila, 1);
            
            try {
                miBD.editAdmin(TextUser.getText(),TextPass.getText());
            } catch (Exception ex) {
                Logger.getLogger(MENU_ADDEVENTOS.class.getName()).log(Level.SEVERE, null, ex);
            }
        }    
        
    }//GEN-LAST:event_GUARDARActionPerformed

