    public void MostrarDatos_Admin() throws Exception{
        
        tDatos.addColumn("User");
        tDatos.addColumn("Pass");
        TablaAdmin.setModel(tDatos);
        
        String [] datos = new String [2];
        
        try{
            ArrayList<Administrador> listaAdmin = new ArrayList();
            
            listaAdmin = CSVCode.cargarAdmin();

            for (Administrador item : listaAdmin){

                datos[0] = item.getUser();
                datos[1] = item.getPassword();
                tDatos.addRow(datos);
            }
            
            TablaAdmin.setModel(tDatos);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e + "No hay administradores en la data base");
        }
           
    }
    
